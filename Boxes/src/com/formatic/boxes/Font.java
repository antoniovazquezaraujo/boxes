	final static int TYPE_24=0;
	final static int TYPE_33=1;
	final static int TYPE_35=2;
	final static int TYPE_45=3;
	final static int TYPE_55=4;
	final static int TYPE_57=5;
	int type;
	int[][][] map;

	
 public Font(int type){
		this.type=type;
		switch(type){

			case TYPE_24: this.map = font24; break;
			case TYPE_33: this.map = font33; break;
			case TYPE_35: this.map = font35; break;
			case TYPE_45: this.map = font45; break;
			case TYPE_55: this.map = font55; break;
			case TYPE_57: this.map = font57; break;
			default     : this.map = font33; break;
		}
	}
	public int [][] getCharMap(int c) {
		if(type==TYPE_24){
			//Special font only for numbers!
			if(Character.isDigit(c)){
				return map[c-'0'];
			}else{
				//ERROR. GESTIONAR ESTO!!!!!!	
				return null;
			}
		}
		switch(c){
			case ' ': return map[0];  
			case '.': return map[1];  
			case ',': return map[2];  
			case 'Ñ': return map[3];  
		}
		if(Character.isDigit(c)){
			return map[4+(c-'0')];
		}else{
			return map[14+ (c - 'A')];
		}
	}
	public int getWidth(){
		return map[0][0].length;
	}
	public int getHeight(){
		return map[0].length;
	}
